    private void menu1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_menu1MouseClicked
        pestanas.setSelectedIndex(0);
        Object[] header = new Object[]{"Nro", "Pregunta", "% de acierto"};
        DefaultTableModel model = new DefaultTableModel(header, 0){
            @Override
            public boolean isCellEditable(int row, int column) {
               return false;
            }
        };
        for(controladora.Pregunta pregunta : recursos.globalAccess.listaDePreguntas){
            model.addRow(new Object[]{recursos.globalAccess.listaDePreguntas.indexOf(pregunta), pregunta.getEnunciado(), pregunta.getPorcentajedeacierto()});
        }
        jTablePreguntas.setModel(model);
        jScrollPanePreguntas.setVisible(true);
        jTablePreguntas.setVisible(true);
    }//GEN-LAST:event_menu1MouseClicked

